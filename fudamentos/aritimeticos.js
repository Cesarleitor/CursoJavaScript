const [a, b, c, d] = [3, 5, 1, 15]  // destructuring array

const soma = a + b + c + d // soma dos valores
const subtracao = d - b // subtracao dos valores
const multiplicacao = a * b // multiplicacao dos valores
const divisao = d / a // divisao dos valores
const modulo = a % 2 // resto da divisao

console.log(soma, subtracao, multiplicacao, divisao, modulo)    // exibindo os resultados no console